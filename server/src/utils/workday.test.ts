import { describe, it, expect } from 'vitest';
import { calculateWorkdays } from './workday';

// 日期辅助：使用正午时间避免夏令时干扰
const d = (iso: string) => new Date(`${iso}T12:00:00`);

describe('calculateWorkdays', () => {
  // ===== 边界情况 =====

  it('开始日期晚于结束日期时返回 0', () => {
    expect(calculateWorkdays(d('2025-01-10'), d('2025-01-05'))).toBe(0);
  });

  it('同一天（周一）返回 1', () => {
    expect(calculateWorkdays(d('2025-01-06'), d('2025-01-06'))).toBe(1);
  });

  it('同一天（周六）返回 0', () => {
    expect(calculateWorkdays(d('2025-01-04'), d('2025-01-04'))).toBe(0);
  });

  it('同一天（周日）返回 0', () => {
    expect(calculateWorkdays(d('2025-01-05'), d('2025-01-05'))).toBe(0);
  });

  // ===== 无节假日的标准周 =====

  it('周一至周五（无节假日）返回 5', () => {
    expect(calculateWorkdays(d('2025-01-06'), d('2025-01-10'))).toBe(5);
  });

  it('周一至周日跨越完整一周返回 5', () => {
    expect(calculateWorkdays(d('2025-01-06'), d('2025-01-12'))).toBe(5);
  });

  it('无节假日的普通周: 2025-03-03(Mon) ~ 2025-03-07(Fri) = 5', () => {
    expect(calculateWorkdays(d('2025-03-03'), d('2025-03-07'))).toBe(5);
  });

  it('从周六到下周五 = 5 工作日', () => {
    expect(calculateWorkdays(d('2025-01-04'), d('2025-01-10'))).toBe(5);
  });

  // ===== 中国法定节假日 =====

  it('元旦（2025-01-01 周三）为节假日', () => {
    // 2024-12-30(Mon) ~ 2025-01-03(Fri)：5个工作日 - 01-01节假日 = 4
    expect(calculateWorkdays(d('2024-12-30'), d('2025-01-03'))).toBe(4);
  });

  it('元旦当天返回 0', () => {
    expect(calculateWorkdays(d('2025-01-01'), d('2025-01-01'))).toBe(0);
  });

  it('春节假期（2025-01-28 ~ 02-04）全部排除', () => {
    expect(calculateWorkdays(d('2025-01-28'), d('2025-02-04'))).toBe(0);
  });

  it('包含春节假期的区间正确计算', () => {
    // 2025-01-27(Mon)=工作日, 01-28~02-04=春节(0), 02-05~02-07=3工作日 → 4
    expect(calculateWorkdays(d('2025-01-27'), d('2025-02-07'))).toBe(4);
  });

  it('劳动节假期（2025-05-01 ~ 05-05）排除', () => {
    expect(calculateWorkdays(d('2025-05-01'), d('2025-05-05'))).toBe(0);
  });

  it('包含劳动节的区间正确计算', () => {
    // 04-28~04-30=3, 05-01~05-05=0, 05-06~05-09=4 → 7
    expect(calculateWorkdays(d('2025-04-28'), d('2025-05-09'))).toBe(7);
  });

  it('国庆假期（2025-10-01 ~ 10-08）排除', () => {
    expect(calculateWorkdays(d('2025-10-01'), d('2025-10-08'))).toBe(0);
  });

  // ===== 调休补班日 =====

  it('春节调休 2025-01-26（周日）计为工作日', () => {
    expect(calculateWorkdays(d('2025-01-26'), d('2025-01-26'))).toBe(1);
  });

  it('春节调休 2025-02-08（周六）计为工作日', () => {
    expect(calculateWorkdays(d('2025-02-08'), d('2025-02-08'))).toBe(1);
  });

  it('国庆调休 2025-09-28（周日）计为工作日', () => {
    expect(calculateWorkdays(d('2025-09-28'), d('2025-09-28'))).toBe(1);
  });

  it('国庆调休 2025-10-11（周六）计为工作日', () => {
    expect(calculateWorkdays(d('2025-10-11'), d('2025-10-11'))).toBe(1);
  });

  it('普通周六周日（非补班日）仍然排除', () => {
    expect(calculateWorkdays(d('2025-03-08'), d('2025-03-09'))).toBe(0);
  });

  // ===== 2026 年 =====

  it('2026 春节假期排除', () => {
    expect(calculateWorkdays(d('2026-02-16'), d('2026-02-22'))).toBe(0);
  });

  it('2026 春节调休 02-14（周六）计为工作日', () => {
    expect(calculateWorkdays(d('2026-02-14'), d('2026-02-14'))).toBe(1);
  });

  // ===== 跨春节假期（节假日 + 调休 + 周末复合场景） =====

  it('2025-01-31(Fri) ~ 2025-02-03(Mon) = 1（春节假期覆盖 02-01~02-03）', () => {
    // 01-31(Fri)=春节假期, 02-01~02-03=春节假期 → 0
    // 注意：01-31 也是春节假期！
    expect(calculateWorkdays(d('2025-01-31'), d('2025-02-03'))).toBe(0);
  });

  it('连续4周含元旦：2025-01-01 ~ 2025-01-31 = 21（22工作日 - 元旦）', () => {
    // 01-01(节假日), 01-02~01-03(Thu-Fri=2), 01-06~01-10(5), 01-13~01-17(5), 01-20~01-24(5), 01-27~01-31(Mon-Fri=5)
    // 但 01-28~01-31 是春节假期！所以 01-27 = 1, 01-28~01-31 = 0
    // 合计: 0 + 2 + 0(weekend) + 5 + 5 + 5 + 1 = 18
    // 加上 01-26(Sun)是补班日 → 在此范围内
    // 01-26 是补班日 = 1
    // 合计: 0 + 2 + 0 + 5 + 5 + 5 + 1(26) + 1(27) + 0(28-31春节) = 19
    expect(calculateWorkdays(d('2025-01-01'), d('2025-01-31'))).toBe(19);
  });
});
